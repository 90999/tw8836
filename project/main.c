#include "config.h"
#include "cpu.h"
#include "uart.h"

void init_task(void)
{
	init_cpu();
	init_uart();
	
	EA = 1;
	
	while (1)
	{
		put_char('x');
		OSWait(K_TMO, 100);		
	}
}

void main(void)
{
    OSInit();
	
		OSTaskCreate(init_task, NULL, 0);

    while (1)
    {
			;
    }
}
